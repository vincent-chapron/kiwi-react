export class Year {
    _name;
    _start_at;
    _end_at;

    getName() {return this._name};
    setName(name) {this._name = name};

    getStartAt() {return this._start_at};
    setStartAt(start_at) {this._start_at = start_at};

    getEndAt() {return this._end_at};
    setEndAt(end_at) {this._end_at = end_at};
}
